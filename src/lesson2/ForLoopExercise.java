public class ForLoopExercise {
    public static void main(String[] args) {

        int a = 5;
        while (a <= 10) {
            System.out.println(a);
            a++;
        }

        a = 15;
        while (a <= 20) {
            System.out.println(a);
            a++;
        }
    }
}
